package CONTROL_FLOW_LOOPS;
// LIMIT OF INTEGER
// MAX - 2147483647
// MIN - (-2147483648)
public class EXTRA {
    static void main(String[] args) {
        int x = Integer.MAX_VALUE;
        System.out.println(x);
        int y = Integer.MIN_VALUE;
        System.out.println(y);
    }
}
// So we can use long keyword to store integers above limit.
